// POST /api/migrate
// Imports IndexedDB export data into Supabase Postgres for the authenticated user.
// Maps camelCase JS fields to snake_case Postgres columns.
// Handles foreign key remapping (old auto-increment IDs → new Postgres IDs).

const { authenticate, getServiceClient } = require('./_lib/auth');

// Field mapping: IndexedDB camelCase → Postgres snake_case
