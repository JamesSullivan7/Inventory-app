// IndexedDB store name → Postgres table name
const TABLE_MAP = {
  products: 'products',
  materials: 'materials',
  recipes: 'recipes',
  suppliers: 'suppliers',
  purchaseOrders: 'purchase_orders',
  batches: 'batches',
  productionRuns: 'production_runs',
  waste: 'waste',
  locations: 'locations',
  history: 'history',
  expenses: 'expenses',
  transactions: 'transactions',
  settings: 'settings',
};

function mapRecord(storeName, record, businessId) {
  const fieldMap = FIELD_MAPS[storeName] || {};
  const mapped = { business_id: businessId };

  for (const [key, value] of Object.entries(record)) {
    if (key === 'id') continue; // Skip old auto-increment IDs
    const pgKey = fieldMap[key] || key;
    mapped[pgKey] = value;
  }

  return mapped;
}

module.exports = async (req, res) => {
  if (req.method !== 'POST') {
    return res.status(405).json({ error: 'Method not allowed' });
  }

  try {
    const { userId, businessId } = await authenticate(req);
    const { data: exportData } = req.body;

    if (!exportData) {
      return res.status(400).json({ error: 'Missing data in request body' });
    }

    const supabase = getServiceClient();
    const results = {};
    const idMaps = {}; // { storeName: { oldId: newId } }

    // Import order matters for foreign keys
    const importOrder = [
      'suppliers', 'locations', 'products', 'materials', 'recipes',
      'purchaseOrders', 'batches', 'productionRuns', 'waste',
      'history', 'expenses', 'transactions', 'settings',
    ];

    for (const storeName of importOrder) {
      const records = exportData[storeName];
      if (!records || !records.length) {
        results[storeName] = 0;
        continue;
      }

      const tableName = TABLE_MAP[storeName];
      if (!tableName) continue;

      // Special handling for settings (key-value store)
      if (storeName === 'settings') {
        for (const rec of records) {
          const { error } = await supabase.from('settings').upsert({
            business_id: businessId,
            key: rec.key,
            value: rec.value != null ? JSON.stringify(rec.value) : null,
          }, { onConflict: 'business_id,key' });
          if (error) console.warn('Settings import error:', error.message);
        }
        results[storeName] = records.length;
        continue;
      }

      // Map all records
      const mapped = records.map(r => mapRecord(storeName, r, businessId));

      // Insert and get back IDs for FK remapping
      const { data: inserted, error } = await supabase
        .from(tableName)
        .insert(mapped)
        .select('id');

      if (error) {
        console.error(`Error importing ${storeName}:`, error.message);
        results[storeName] = { error: error.message };
        continue;
      }

      // Build ID map (old position-based → new Postgres ID)
      idMaps[storeName] = {};
      records.forEach((original, i) => {
        if (inserted[i]) {
          idMaps[storeName][original.id] = inserted[i].id;
        }
      });

      results[storeName] = inserted.length;
    }

    return res.status(200).json({
      success: true,
      imported: results,
      message: 'Data migration complete',
    });
  } catch (error) {
    console.error('Migration error:', error.message);
    const status = error.status || 500;
    return res.status(status).json({ error: error.message });
  }
};
